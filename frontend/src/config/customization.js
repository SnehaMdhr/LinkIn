export function getGradientCSS(value, customFrom, customTo) {
  if (value === "custom") {
    return `linear-gradient(135deg, ${customFrom || "#84cc16"}, ${customTo || "#10b981"})`;
  }
  const g = GRADIENTS.find((g) => g.value === value);
  return g ? g.css : GRADIENTS[0].css;
}

export function getBlurValue(blur) {
  switch (blur) {
    case "none": return "0px";
    case "low": return "8px";
    case "medium": return "18px";
    case "high": return "30px";
    default: return "18px";
  }
}
